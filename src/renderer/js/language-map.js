export function languageFromFileName(name) {
  const lowerName = String(name || '').toLowerCase();
  if (!lowerName) return 'plaintext';

  const byName = FILENAME_LANGUAGE_MAP[lowerName];
  if (byName) return byName;

  for (const key of EXTENSION_LANGUAGE_KEYS) {
    if (lowerName === key || lowerName.endsWith(`.${key}`)) {
      return EXTENSION_LANGUAGE_MAP[key];
    }
  }

  return 'plaintext';
}

export function languageLabelFromFileName(name) {
  const language = languageFromFileName(name);
  return LANGUAGE_LABEL_MAP[language] || language || 'Text';
}
